/** @param {Record<string, unknown>} base @param {Record<string, unknown>} over */
function deepMerge(base, over) {
  if (!over) return base;
  const out = { ...base };
  for (const k of Object.keys(over)) {
    const bv = base[k];
    const ov = over[k];
    if (
      ov &&
      typeof ov === 'object' &&
      !Array.isArray(ov) &&
      bv &&
      typeof bv === 'object' &&
      !Array.isArray(bv)
    ) {
      out[k] = deepMerge(/** @type {Record<string, unknown>} */ (bv), /** @type {Record<string, unknown>} */ (ov));
    } else {
      out[k] = ov;
    }
  }
  return out;
}

